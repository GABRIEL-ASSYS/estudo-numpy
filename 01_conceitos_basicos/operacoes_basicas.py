import numpy as np

# Criando dois arrays
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# Operações matemáticas entre arrays
print("Array A:", a)
print("\nArray B:", b)
print("\nSoma (A + B):", a + b)
print("\nSubtração (A - B):", a - b)
print("\nMultiplicação (A * B):", a * b)
print("\nDivisão (A / B):", a / b)

# Operações com escalares
print("\nOperações com escalares:")
print("A * 2:", a * 2)
print("A + 10:", a + 10)